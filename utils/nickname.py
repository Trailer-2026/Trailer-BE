"""랜덤 닉네임 생성 — 소셜 회원가입 시 기본 닉네임을 자동 부여한다.

여행 테마 '형용사 + 명사 + 4자리 숫자' 조합(예: 설레는갈매기4821). 조합 수가 커서
(형용사 × 명사 × 9000) 충돌이 드물다. nickname 컬럼은 유니크 제약이 없어 완전 고유는
보장하지 않지만, 사용자가 나중에 변경하는 전제라 이 정도로 충분하다.
"""
import random

_ADJECTIVES = [
    "행복한", "느긋한", "설레는", "떠나는", "자유로운", "용감한", "따뜻한", "상쾌한",
    "반짝이는", "포근한", "신나는", "여유로운", "든든한", "활기찬", "잔잔한", "청량한",
]
_NOUNS = [
    # 여행/탐험
    "여행자", "나그네", "방랑자", "탐험가", "순례자", "몽상가", "산책가", "길잡이",
    "나침반", "등대", "기차", "돛단배", "배낭", "지도", "텐트", "자전거",
    # 동물
    "갈매기", "여우", "고래", "산양", "다람쥐", "수달", "부엉이", "펭귄",
    "물개", "사슴", "토끼", "너구리", "돌고래", "판다", "청설모", "반달곰",
    # 자연/하늘
    "구름", "바람", "파도", "별빛", "노을", "안개", "무지개", "단풍",
    "은하수", "오로라", "새싹", "함박눈", "이슬", "물결", "햇살", "달빛",
]


def generate_nickname() -> str:
    """랜덤 닉네임 1개. 형용사+명사+4자리 숫자 (예: '설레는갈매기4821')."""
    return f"{random.choice(_ADJECTIVES)}{random.choice(_NOUNS)}{random.randint(1000, 9999)}"


if __name__ == "__main__":
    # 형식·다양성 최소 검증: 여러 번 뽑아 대부분 서로 다르고 형식이 맞는지.
    import re
    samples = [generate_nickname() for _ in range(20)]
    assert all(re.fullmatch(r"[가-힣]+\d{4}", n) for n in samples), samples
    assert len(set(samples)) >= 15, f"다양성 부족: {samples}"
    print("nickname selfcheck OK:", samples[:5])
